LUNAConfig = {} -- Global variable for easy referencing. 

LUNAConfig.MoneyUiEnabled = false; -- Set to false to disable Money in the top right corner. 
LUNAConfig.SurvivalUiEnabled = false; -- Controls the UI under the healthbar.
LUNAConfig.EnableComa = true; -- Controls the LUNA coma on death.
LUNAConfig.EnableFoodAndWater = false; -- Controls the food and water system.
LUNAConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
LUNAConfig.EnableBuyVehicles = false; -- Enables ability to buy vehicles from the RageUI Garages.  
LUNAConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
LUNAConfig.VehicleStoreRadius = 50; -- Controls radius a vehicle can be stored from.
LUNAConfig.AdminCoolDown = true; -- Enables an admin cooldown on call admin.
LUNAConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
LUNAConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
LUNAConfig.DoNotDisplayIps = false; -- Removes all LUNA related references in the console to player ip addresses.
LUNAConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
LUNAConfig.AllowMoreThenOneCar = true; -- Controls if you can have more than one car out.
LUNAConfig.F10System = true; -- Logs warnings and can be accessed via F10 (Thanks to Rubbertoe98) (https://github.com/rubbertoe98/FiveM-Scripts/tree/master/LUNA_punishments)
LUNAConfig.ServerName = "LUNA" -- Controls the name that is displayed on warnings title etc.
LUNAConfig.PlayerSavingTime = 3000 -- Time in milliseconds to update Player saving
---------------
LUNAConfig.LootBags = true; -- Enables loot bags and disables looting. 
LUNAConfig.DisplayNamelLootbag = true; -- Enables notification of who's lootbag you have opened
