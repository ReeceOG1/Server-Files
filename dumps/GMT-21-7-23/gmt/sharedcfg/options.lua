GMTConfig.SurvivalUiEnabled = true; -- Controls the UI under the healthbar.
GMTConfig.EnableComa = true; -- Controls the GMT coma on death.
GMTConfig.EnableFoodAndWater = false; -- Controls the food and water system.
GMTConfig.EnableHealthRegen = false; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
GMTConfig.EnableBuyVehicles = true; -- Enables ability to buy vehicles from the RageUI Garages.  
GMTConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
GMTConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
GMTConfig.AdminCoolDown = false; -- Enables an admin cooldown on call admin.
GMTConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
GMTConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
GMTConfig.DoNotDisplayIps = true; -- Removes all GMT related references in the console to player ip addresses.
GMTConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
GMTConfig.AllowMoreThenOneCar = false; -- Controls if you can have more than one car out.
GMTConfig.F10System = true; -- Logs warnings and can be accessed via F10 
